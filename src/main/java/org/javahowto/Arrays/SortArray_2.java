package org.javahowto.Arrays;

import java.util.Arrays;

public class SortArray_2 {
    public static void main(String[] args) {
        String[] cars = {"Volvo", "BMW", "Tesla", "Ford", "Fiat", "Mazda", "Audi"};

        Arrays.toString(cars);
        for(String i : cars) {
            System.out.println(i);
        }
    }
}
